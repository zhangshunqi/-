#include<stdio.h>
int main(){
    int a;
    int num = 0, o=0, p=0, q=0;
    int b=0, c=0, d=0, e=0, f=0, g=0;
    while(scanf("%d ", &a)==1){
        switch (a){
            case 1:b+=1000;e++;break;
            case 2:c+=500;f++;break;
            case 3:d+=200;g++;break;
        }
    }
    num = b+c+d;
    if(e/3>0){
        o=1000*(e/3);
    }
    if(f/6>0){
        p=500*(f/6);
    }
    if(g/10>0){
        q=200*(g/10);
    }
    num = num+o+p+q;
    printf("%d", num);
    return 0;
}
